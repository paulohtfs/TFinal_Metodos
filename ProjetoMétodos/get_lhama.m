function lhama = get_lhama(current_level)

switch current_level
    case 0
        lhama = 'no_lhama.jpg';
    
    case 1
        lhama = 'lhama.jpg';
        
    case 2
        lhama = 'super_lhama.jpg';
        
    case 3
        lhama = 'lhama_albina.jpg';
        
    case 4
        lhama = 'super_lhama_albina.jpg';
        
    case 5
        lhama = 'lhama_ninja.jpg';
        
    case 6
        lhama = 'lhama_rei.jpg';
        
    case 7
        lhama = 'lhama_sparta.jpg';
        
    case 8
        lhama = 'lhama_mago.jpg';
        
    case 9
        lhama = 'lhama_ouro.jpg';
end